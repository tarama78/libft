#include <libft.h>

int			ft_strnequ(const char *s1, const char *s2, size_t n)
{
	while (*s1 == *s2 && *s1 && n-- > 0)
	{
		s1++;
		s2++;
	}
	if (*s1 == *s2)
		return (1);
	return (0);
}
